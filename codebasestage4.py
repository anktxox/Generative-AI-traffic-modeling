def allowed_lanes_record(adj_mat, veh_type):
    allowed_lanes = []
    mat = adj_mat[0][11 + veh_type]
    for i in range(len(mat)):
        for j in range(len(mat[0])):
            if mat[i][j] > 1e-5:
                allowed_lanes.append((i, j))
    return allowed_lanes

